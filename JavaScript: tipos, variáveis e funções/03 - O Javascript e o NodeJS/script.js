// tipagem dinâmica

let minhaVar = 567;
minhaVar = "texto"; 

/*O fato de eu poder mudar o valor de uma variável para o valor que eu quiser sem a necessidade de declarar o tipo de dado (ou seja, untyped), mesmo que o mesmo mude,
faz com que o Javascript seja considerada uma linguagem de tipagem dinâmica */

// multiparadigma => ou seja, é capaz de resolver um determinado problema de várias maneiras diferentes

/* O que é ES6 / ES2015? -> ES6 (ou ES2015) é a versão do Javascript que foi lançada em 2015, ela foi a acabou sendo a versão mais importante do js, pois trouxe
muitas funcionalidades (let e const, por exemplo) importantes o que levou o js para outro nível */

/* Linguagem interpretada e compilada => na interpretada o nosso código será executado, então terá um programa por trás que vai ler as linhas e executá-lo.
Já nas linguagens compiladas, o código vai passar por um compilador, que transformará o nosso código em linguagem de máquina, e quando o código for executado,
ele será executado diretamente em linguagem de máquina */

/* O que é o Node? =>  Como o JS é uma linguagem interpretada, ela necessita de um interpretador, nesse sentido nós podemos usar o navegador como interpretador
ou podemos usar o Node. Se usarmos o Node, nós estamos mexendo com o lado do backend, ele também traz um console. Ele foi criado para as pessoas conseguirem rodar
o JS fora do navegador */